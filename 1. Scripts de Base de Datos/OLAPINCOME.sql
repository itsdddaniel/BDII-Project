CREATE DATABASE OLAPINCOME;
USE OLAPINCOME;

CREATE TABLE D_VIAJES (
  ID_VIAJE INT PRIMARY KEY NOT NULL,
  DIRECCION VARCHAR(1000),
  MONTO INT, 
  ESTADO BIT
);

CREATE TABLE D_SUCURSAL (
  ID_SUCURSAL INT PRIMARY KEY NOT NULL,
  DIRECCION VARCHAR(1000)
);

