  CARGA VARCHAR(1000),
  TIPO VARCHAR(1000)
);

CREATE TABLE D_EMPLEADOS (
  ID_EMPLEADO INT PRIMARY KEY NOT NULL,
  NOMBRE VARCHAR(1000)
);

